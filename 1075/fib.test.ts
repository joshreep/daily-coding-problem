import fib from './fib'

test('should should return the proper number in the Fibonacci sequence', () => {
    expect(fib(0)).toBe(0)
    expect(fib(1)).toBe(1)
    expect(fib(2)).toBe(1)
    expect(fib(3)).toBe(2)
    expect(fib(4)).toBe(3)
    expect(fib(5)).toBe(5)
    expect(fib(6)).toBe(8)
    expect(fib(7)).toBe(13)
    expect(fib(8)).toBe(21)
    expect(fib(9)).toBe(34)
    expect(fib(10)).toBe(55)
    expect(fib(11)).toBe(89)
    expect(fib(12)).toBe(144)
    expect(fib(13)).toBe(233)
    expect(fib(14)).toBe(377)
    expect(fib(15)).toBe(610)
    expect(fib(16)).toBe(987)
    expect(fib(17)).toBe(1597)
    expect(fib(18)).toBe(2584)
    expect(fib(19)).toBe(4181)
    expect(fib(20)).toBe(6765)
    expect(fib(30)).toBe(832040)
    expect(fib(40)).toBe(102334155)
    expect(fib(50)).toBe(12586269025)
    expect(fib(60)).toBe(1548008755920)
    expect(fib(70)).toBe(190392490709135)
    // after 70 we get into big int territory and we start to see the effects of the inaccuracy of Javascript
})
